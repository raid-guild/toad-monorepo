/*
# TOAD Landing Page - Figma Implementation Guide

## Step 1: Set Up Design System

1. Create a new Figma file named "TOAD Landing Page"
2. Create a page called "Design System"
3. Set up the following frames:
   - Colors
   - Typography
   - Spacing
   - Components

### Colors
Create color styles for:
- Primary (Green) palette: 50-900
- Neutral (Gray) palette: 50-950
- White and Black

### Typography
Create text styles for:
- Headings (H1-H6)
- Body (Regular, Medium, Small)
- Button text
- Links

### Components
Create component variants for:
- Buttons (Primary, Secondary, Ghost)
- Navigation links (Default, Active, Hover)
- Cards
- Form inputs
- Icons

## Step 2: Create Page Layout Frames

1. Create a new page called "Desktop"
2. Add frames for each section:
   - Navbar (1440×64px)
   - Hero (1440×600px)
   - About (1440×800px)
   - Ecosystem (1440×1000px)
   - Contact (1440×800px)
   - Footer (1440×400px)

3. Create a new page called "Mobile"
4. Add frames for mobile versions (375px width)

## Step 3: Design Navbar

1. Create a rectangle for the navbar background (1440×64px)
2. Add the TOAD logo (40×40px)
3. Add "TOAD" text using the brand font
4. Add navigation links (About, Ecosystem, Contact)
5. Add theme toggle button
6. Add "Get Started" button
7. For mobile, add a hamburger menu icon

## Step 4: Design Hero Section

1. Add the banner image as background with reduced opacity (20%)
2. Add a gradient overlay from transparent to white/dark background
3. Place the TOAD logo (96×96px)
4. Add heading "TOAD" using H1 style
5. Add subtitle "Trustless On-Chain Autonomous Delegate"
6. Add description text
7. Add two buttons: "Explore TOAD" and "View Documentation"

## Step 5: Design About Section

1. Create a heading "About TOAD"
2. Add description text
3. Create a 2×2 grid of feature cards:
   - Community Sentiment Analysis
   - Intelligent Delegation
   - AI-Powered Insights
   - Trustless Architecture
4. Each card should include:
   - Icon
   - Heading
   - Description text

## Step 6: Design Ecosystem Section

1. Create a heading "Ecosystem"
2. Add description text
3. Create a row of 4 platform cards:
   - Forums
   - Twitter
   - Warpcast
   - Discord
4. Add "How TOAD Works" info box with:
   - Heading
   - Description text
   - TOAD logo visualization

## Step 7: Design Contact Section

1. Create a heading "Contact Us"
2. Add description text
3. Create a contact form with:
   - Name field
   - Email field
   - Subject field
   - Message textarea
   - "Send Message" button

## Step 8: Design Footer

1. Create a dark background rectangle
2. Add TOAD logo and name
3. Add description text
4. Create 3 columns for links:
   - Navigation
   - Resources
   - Legal
5. Add social media icons
6. Add copyright text

## Step 9: Create Dark Mode Variants

1. Duplicate all frames
2. Apply dark mode color scheme
3. Adjust text colors for readability
4. Invert the logo for dark backgrounds
5. Adjust background colors and borders

## Step 10: Create Interactive Prototype

1. Connect frames with interactions
2. Add scrolling behavior
3. Add hover states for buttons and links
4. Create theme toggle interaction

## Step 11: Prepare for Export

1. Organize layers and frames
2. Name all components consistently
3. Create documentation for developers
4. Export necessary assets (logo, icons, images)

## Assets Required

1. TOAD Logo (PNG with transparency)
2. Banner image (Forest scene)
3. Icons:
   - Message Square
   - Vote
   - Users
   - Bot
   - Arrow Right
   - Send
   - GitHub
   - Twitter
   - External Link
   - Menu/Hamburger
   - X (close)
   - Sun/Moon (theme toggle)
*/
